export const extractTailwindConfig = async(req:Request, res: Response): Promise<void> =>{
    try{
        const {colors, fonts, fontSize, spacing, sections} = req.body;

        if(!colors || !fonts || !fontSize || !spacing){
            res.status(400).json({
                success:false,
                message:"Missing extraction parameters. colors, fonts, fontsize, and spacing are all required"
            });
            return;
        }
         
        let colorLines = '';
        if(sections && Object.keys(sections).length > 0){
            colorLines = Object.entries(sections)
            .map(([sectionName, sectionData]:[string, any]) =>{
                const sectionColors = sectionData.colors || [];
                if(sectionColors.length === 0) return '';

                const sectionKey = sectionName.toLowerCase().replace(/[^a-z0-9]+/g, '-');

                const mappedSectionColors = sectionColors.map((color:string, idx: number) =>
                     `'${sectionKey}-${idx + 1}': '${color}'`
                ).join(',\n        ');
               
                return `/* ${sectionName} Colors */\n        ${mappedSectionColors}`;
            })
            .filter(Boolean)
            .join(',\n\n        ');
        }else{
            colorLines = colors.map((color: string, idx: number)=> `'color-token-${idx + 1}': '${color}'`).join(',\n        ');
        }
        
        //  Process font Sizes
        const uniqueFontSizes: Record<string, string> ={};
        fontSize.forEach(([key, value]: [string, string], idx: number) => {
            if(!key) return ;
            const baseKey = key.replace(/\s+g/g,'-').toLowerCase();
            const uniqueKey = uniqueFontSizes[baseKey]? `${baseKey}-${idx + 1}` : baseKey;
            uniqueFontSizes[uniqueKey] = value;
        });
          
        // Process spacing
         const uniqueSpacing :Record<string, string>={};
         spacing.forEach(([key, value]:[string, string], idx: number)=>{

            if(!key) return;

            let cleanKey = key
            .replace(/-\$\s*\{s*s\.property\s*\}/g, '')
            .replace(/\s+/g, '')
            .toLowerCase()
            .trim();

            if(!cleanKey || cleanKey === '-'){
                cleanKey = 'space-token';
            }

            const finalKey = `${cleanKey}-${idx + 1}`;
            uniqueSpacing[finalKey] = value;

         });

        //  Generate Configuratrion String with section-grouped colors
         const generatedFileText = `/** @type {import('tailwindcss').Config} */
         /** Auto-generated by Tailwind Config Extractor */
         
         module.exports = {
           theme:{
            extend: {
              colors: {
                 ${colorLines}
              },
              fontFamily: {
                 ${fonts.map((font: string, idx: number) => `'site-${idx + 1}': ['${font}', 'sans-serif']`).join(',\n        ')}
              },
              fontSize: {
                 ${Object.entries(uniqueFontSizes).map(([key, val]) => `'${key}': ['${val}', { lineHeight: '1.5' }]`).join(',\n        ')}
               },
               spacing: {
                 ${Object.entries(uniqueSpacing).map(([key, val]) => `'${key}': '${val}'`).join(',\n        ')}
                },
            }
          }
         
         },
         plugins:[],

        };`;

        res.status(200).json({
             success: true, 
             message:"Tailwind Configuration file cleaned and generated successfully.",
             configFileContent: generatedFileText.trim()
        });
    } catch (err: any){
        console.error("Extractor engine exception:", err.message);
        res.status(500).json({
            success: false,
            message:"Internal server Error while building configuration file."
        });
    }
};

